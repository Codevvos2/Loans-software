    private void b_cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_cadastrarActionPerformed
      try {
           
           
            int quantidade = 0;
            String dataloc = "";
            String datadev = "";
            String status = "";
             int idc = 0;
            int idf = 0;
            if (this.c_idc.getText().length() <= 0) {
                throw new Mensagens("ID cliente deve conter ao menos 1 número.");
            } else {
                idc =Integer.parseInt( this.c_idc.getText());
            }

            if (this.c_idf.getText().length() <= 0) {
                throw new Mensagens("ID ferramenta deve conter ao menos 1 número.");
            } else {
                idf = Integer.parseInt (this.c_idf.getText());
            }
            
            if (this.c_quantidade.getText().length() <= 0) {
                throw new Mensagens("quantidade deve conter ao menos 1 caracter.");
            } else {
                quantidade = Integer.parseInt( this.c_quantidade.getText());
            }

            if (this.c_dataloc.getText().length() <= 0) {
                throw new Mensagens("Data de locação deve conter ao menos 2 caracteres.");
            } else {
                dataloc = this.c_dataloc.getText();
            }
             if (this.c_datadev.getText().length() <= 0) {
                throw new Mensagens("Data de devolução deve conter ao menos 2 caracteres.");
            } else {
                datadev = this.c_datadev.getText();
            }
             if (this.c_status.getText().length() <= 0) {
                throw new Mensagens("Status deve conter ao menos 2 caracteres.");
            } else {
                status= this.c_status.getText();
            }

            if (this.objemprestimo.InsertEmprestimoBD( quantidade, dataloc, datadev, status,idc, idf)) {
                JOptionPane.showMessageDialog(rootPane, "Empréstimo Cadastrado com Sucesso!");

              
               
                this.c_quantidade.setText("");
                this.c_dataloc.setText("");
                this.c_datadev.setText(""); 
                this.c_status.setText("");
                 this.c_idc.setText("");
                this.c_idf.setText("");
            }

            System.out.println(this.objemprestimo.getListaEmprestimo().toString());

        } catch (Mensagens erro) {
            JOptionPane.showMessageDialog(null, erro.getMessage());
        } catch (NumberFormatException erro2) {
            JOptionPane.showMessageDialog(null, "Informe um número.");
        } catch (SQLException ex) {
            Logger.getLogger(CadastroCliente.class.getName()).log(Level.SEVERE, null, ex);
        }


               
    }//GEN-LAST:event_b_cadastrarActionPerformed

