  public void getSum(){
  double  sum = 0;
  for(int i = 0;i < tblferramenta.getRowCount(); i++){
      sum =sum + Double.parseDouble(tblferramenta.getValueAt(i, 3).toString());
  }
   c_quantidade.setText(Double.toString(sum));
  }
