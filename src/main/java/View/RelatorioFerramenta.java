   private void PreencheTabela(){
       FerramentaDAO FerramentaDAO = new FerramentaDAO();
       List<Ferramenta> ListaFerramentas = FerramentaDAO.getListaFerramenta();
  DefaultTableModel tabelaFerramenta = (DefaultTableModel) tblferramenta.getModel();
  tabelaFerramenta.setNumRows(0);
  for(Ferramenta a : ListaFerramentas){
    Object[] obj = new Object[]{
      a.getIdf(),
        a.getNome(),
        a.getMarca(),
        a.getValor(),
        a.getSetor(),
        a.getEstoque()
    };
    
   tabelaFerramenta.addRow(obj);
    } 
  
   }
  

