    @SuppressWarnings("unchecked")
    public void carregaTabela() {

        DefaultTableModel modelo = (DefaultTableModel) this.jTableEmprestimo.getModel();
        modelo.setNumRows(0);

        ArrayList<Emprestimo> listaemprestimo = new ArrayList<>();
        listaemprestimo = objemprestimo.getListaEmprestimo();

        for (Emprestimo a : listaemprestimo) {
            modelo.addRow(new Object[]{
                a.getIde(),
                a.getQuantidade(),
                a.getDataloc(),
                a.getDatadev(),
                a.getStatus(),
                a.getIdc(),
                a.getIdf(),});
        }
    }

