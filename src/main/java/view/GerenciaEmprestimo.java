    private void b_alterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_alterarActionPerformed
        try {

            int ide = 0;
            int quantidade = 0;
            String dataloc = "";
            String datadev = "";
            String status = "";
            int idc = 0;
            int idf = 0;

            if (this.c_quantidade.getText().length() <= 0) {
                throw new Mensagens("Quantidade deve ser um número maior que zero.");
            } else {
                quantidade = Integer.parseInt(this.c_quantidade.getText());
            }

            if (this.c_dataloc.getText().length() <= 0) {
                throw new Mensagens("data locação deve conter um caractere.");
            } else {
                dataloc = this.c_dataloc.getText();
            }

            if (this.c_datadev.getText().length() <= 0) {
                throw new Mensagens("data devolução deve conter um caractere.");
            } else {
                datadev = this.c_datadev.getText();
            }

            if (this.c_status.getText().length() <= 0) {
                throw new Mensagens("Status deve ser conter um caractere.");
            } else {
                status = this.c_status.getText();
            }

            if (this.jTableEmprestimo.getSelectedRow() == -1) {
                throw new Mensagens("Primeiro Selecione um Empréstimo para Alterar");
            } else {
                ide = Integer.parseInt(this.jTableEmprestimo.getValueAt(this.jTableEmprestimo.getSelectedRow(), 0).toString());
            }
            if(this.jTableEmprestimo.getSelectedRow() == -1){
                throw new Mensagens("selecione um id válido");                
            }else {
                idf = Integer.parseInt(this.jTableEmprestimo.getValueAt(this.jTableEmprestimo.getSelectedRow(), 6).toString());
           }
            if(this.jTableEmprestimo.getSelectedRow() <0){
                throw new Mensagens("selecione um id válido");                
            }else {
                idc = Integer.parseInt(this.jTableEmprestimo.getValueAt(this.jTableEmprestimo.getSelectedRow(), 5).toString());
            }

            if (this.objemprestimo.UpdateEmprestimoBD(ide, quantidade, dataloc, datadev, status, idc, idf)) {

                // limpa os campos
                this.c_quantidade.setText("");
                this.c_dataloc.setText("");
                this.c_datadev.setText("");
                this.c_status.setText("");
                JOptionPane.showMessageDialog(rootPane, "Emprestimo Alterado com Sucesso!");

            }
            System.out.println(this.objemprestimo.getListaEmprestimo().toString());
        } catch (Mensagens erro) {
            JOptionPane.showMessageDialog(null, erro.getMessage());
        } catch (NumberFormatException erro2) {
            JOptionPane.showMessageDialog(null, "Informe um número.");
        } finally {
            carregaTabela();
        }
    }//GEN-LAST:event_b_alterarActionPerformed

