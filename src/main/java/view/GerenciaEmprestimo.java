    private void b_apagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_apagarActionPerformed
        try {

            int ide = 0;
            if (this.jTableEmprestimo.getSelectedRow() == -1) {
                throw new Mensagens("Primeiro Selecione um Emprestimo para APAGAR");
            } else {
                ide = Integer.parseInt(this.jTableEmprestimo.getValueAt(this.jTableEmprestimo.getSelectedRow(), 0).toString());
            }

            int resposta_usuario = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja APAGAR este Emprestimo ?");

            if (resposta_usuario == 0) {

                if (this.objemprestimo.DeleteEmprestimoBD(ide)) {

                    this.c_quantidade.setText("");
                    this.c_dataloc.setText("");
                    this.c_datadev.setText("");
                    this.c_status.setText("");
                    
                    JOptionPane.showMessageDialog(rootPane, "Emprestimo Apagado com Sucesso!");

                }

            }

            System.out.println(this.objemprestimo.getListaEmprestimo().toString());

        } catch (Mensagens erro) {
            JOptionPane.showMessageDialog(null, erro.getMessage());
        } finally {

            carregaTabela();
        }
    }//GEN-LAST:event_b_apagarActionPerformed

